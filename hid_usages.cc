bool
Usage::
is_modifier() const
{
  return (id >= 0xe0 && id <= 0xe7);
}

const Usage &
Usage::
lookup(const std::wstring &key, const std::wstring &page)
{
  Usage (*usages)[] = NULL;
  if (page == L"Keyboard_and_Keypad")
    usages = &keyboard_usages;
  else if (page == L"Generic_Desktop")
    usages = &generic_desktop_usages;
  else if (page == L"Consumer_Control")
    usages = &consumer_control_usages;
  else
    return (*usages)[1]; // Unknown usage page;

  int index = 0;
  Usage *usage;
  while (!(usage = &(*usages)[index])->key.empty())
  {
     if (usage->key == key)
       return *usage;
     ++index;
  }
  return (*usages)[0]; // Usage not found
}

